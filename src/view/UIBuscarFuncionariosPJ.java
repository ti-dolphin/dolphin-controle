    public UIBuscarFuncionariosPJ(UICadFeriasFuncionarioPJ uiCadFerias) {
        initComponents();
        this.uiCadFerias = uiCadFerias;
        this.fs = ServicosFactory.getFUNCIONARIOSERVICOS();
        funcionariosPJTableModel = new FuncionariosTableModel();
        sorter = new TableRowSorter<>(funcionariosPJTableModel);
        jtFuncionariosPJ.setRowSorter(sorter);
        buscar();
        pesquisar();
        configurarTabela();
    }

    private void configurarTabela() {
        jtFuncionariosPJ.getTableHeader().setResizingAllowed(false);
        jtFuncionariosPJ.setAutoResizeMode(jtFuncionariosPJ.AUTO_RESIZE_OFF);
        jtFuncionariosPJ.getColumnModel().getColumn(0).setPreferredWidth(50);
        jtFuncionariosPJ.getColumnModel().getColumn(1).setPreferredWidth(464);
        jtFuncionariosPJ.getColumnModel().getColumn(2).setPreferredWidth(50);
    }//configurarTabela

    private void buscar() {
        try {

            ArrayList<Funcionario> funcionarios = fs.buscarFuncionariosPJ();

            for (Funcionario funcionario : funcionarios) {
                funcionariosPJTableModel.addRow(funcionario);
            }

            jtFuncionariosPJ.setModel(funcionariosPJTableModel);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,
                    e.getMessage(),
                    "Erro ao buscar funcion√°rios PJ",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//buscar

    private void pesquisar() {
        String nome = jtfNome.getText().trim();
        boolean ativos = jchAtivos.isSelected();
        
        List<RowFilter<Object, Object>> filters = new ArrayList<>();
        filters.add(RowFilter.regexFilter("(?i)" + nome, 1));
        filters.add(RowFilter.regexFilter("(?i)" + ativos, 2));
        
        sorter.setRowFilter(RowFilter.andFilter(filters));
    }

    private void carregarCamposNaTelaDeCadastro() {
        Funcionario funcionario = pegarFuncionarioSelecionado();
        if (funcionario != null) {
            uiCadFerias.setFuncionario(funcionario);
            this.uiCadFerias.getJtfChapa().setText(funcionario.getChapa());
            this.uiCadFerias.getJtfNome().setText(funcionario.getNome());
            this.dispose();
        }
    }

    private Funcionario pegarFuncionarioSelecionado() {
        int linhaSelecionada = jtFuncionariosPJ.getRowSorter().convertRowIndexToModel(jtFuncionariosPJ.getSelectedRow());
        return funcionariosPJTableModel.getRowValue(linhaSelecionada);
    }

