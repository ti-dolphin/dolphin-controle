    public UIBuscarResponsavel(UICadMovimento uiCadMovimento) {
        initComponents();
        this.uiCadMovimento = uiCadMovimento;
        pessoaDAO = new PessoaDAO();
        pessoaTableModel = new PessoaTableModel();
        sorter = new TableRowSorter<>(pessoaTableModel);
        jtPessoas.setRowSorter(sorter);
        buscar();
        configurarTabela();
    }

    private void configurarTabela() {
        jtPessoas.getTableHeader().setResizingAllowed(false);
        jtPessoas.setAutoResizeMode(jtPessoas.AUTO_RESIZE_OFF);
        jtPessoas.getColumnModel().getColumn(0).setPreferredWidth(50);
        jtPessoas.getColumnModel().getColumn(1).setPreferredWidth(282);
        jtPessoas.getColumnModel().getColumn(2).setPreferredWidth(50);
    }
    
    private void buscar() {
        try {
            ArrayList<Pessoa> pessoas = pessoaDAO.buscar();

            for (Pessoa pessoa : pessoas) {
                pessoaTableModel.addRow(pessoa);
            }

            jtPessoas.setModel(pessoaTableModel);

        } catch (Exception e) {
            Logger.getLogger(UIBuscarResponsavel.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(null,
                    e.getMessage(),
                    "Erro ao buscar responsavel",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

    private void pesquisar() {
        String nome = jtfNome.getText().trim();
        boolean ativos = jchAtivos.isSelected();

        List<RowFilter<Object, Object>> filters = new ArrayList<>();
        filters.add(RowFilter.regexFilter("(?i)" + nome, 1));
        filters.add(RowFilter.regexFilter("(?i)" + ativos, 2));

        sorter.setRowFilter(RowFilter.andFilter(filters));
    }

    private Pessoa pegarPessoaSelecionado() throws IndexOutOfBoundsException {
        try {
            int linhaSelecionada = jtPessoas.getRowSorter().convertRowIndexToModel(jtPessoas.getSelectedRow());

            return pessoaTableModel.getRowValue(linhaSelecionada);

        } catch (IndexOutOfBoundsException e) {
            throw new IndexOutOfBoundsException("Selecione a pessoa!");
        }

    }

    private void carregarCamposNaTelaDeCadastro() {
        try {
            Pessoa pessoa = pegarPessoaSelecionado();
            if (pessoa != null) {
                uiCadMovimento.setResponsavel(pessoa);
                this.uiCadMovimento.getJtfResponsavel().setText(pessoa.getNome());
                this.dispose();
            }
        } catch (IndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(),
                    "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }

